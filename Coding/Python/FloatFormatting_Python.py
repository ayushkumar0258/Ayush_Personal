data=100/599
print("Float value of 100/599 is : ", data) # This will print the float value of the division of 100 by 599, which is approximately 0.166944907. The output will be: Float value of 100/599 is :  0.166944907 
print("Value of data after formatting {d}".format(d=data))
print("Value of data after formatting {d:2.4f}".format(d=data)) #  This will format the float value of data to 4 decimal places. The output will be: Value of data after formatting 0.1669          
print("Value of data after formatting {d:2.2f}".format(d=data)) # This will format the float value of data to 2 decimal places. The output will be: Value of data after formatting 0.17
