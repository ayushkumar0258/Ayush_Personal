my_new_list.append(56) #### it will append 56 in the last of the list
print("list values after appending are following. : ", my_new_list)
####################### pop method for removing last item from the list #################
my_new_list.pop()
print("list values after appending are following. : ", my_new_list)
my_new_list.pop()
print("list values after appending are following. : ", my_new_list)
my_new_list.pop()
