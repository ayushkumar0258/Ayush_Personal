####### While Loop in Python #######
# Syntax of while loop
# while condition:
#     statement 
i=1
while i<=10:     ## this will print the value from 1 to 10 because we are using while loop and the condition is i<=10 and we are incrementing the value of i by 1 in each iteration.
    print(i)
    i+=1
j=1
while j<=10:     ## this will print the value from 1 to 10 because we are using while loop and the condition is j<=10 and we are incrementing the value of j by 2 in each iteration.
    print(j)
    j+=2
k=10
while k>0:      ## this will print the value from 10 to 1 because we are using while loop and the condition is k>0 and we are decrementing the value of k by 1 in each iteration.
    print(k)
    k-=1
l=10
while l>0:      ## this will print the value from 10 to 1 because we are using while loop and the condition is l>0 and we are decrementing the value of l by 2 in each iteration.
    print(l)
    l-=2            
